     */
    public function store(StoreUserRequest $request)
    {

        $role_user = Role::where('name', 'user')->first();
        $user=User::create([
            'name'=>$request->name,
            'email'=>$request->email,
            'password'=>Hash::make('secret'),
        ]);
        //by default role_user
        $user->roles()->attach($role_user);
        return redirect()->route('users.index');
    }
